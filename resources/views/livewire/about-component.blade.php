          <h3>presentation de l'ESATIC</h3>

          <p class="text">Créée par le décret n° 2012-20 du 18 Janvier 2012, l’Ecole Supérieure Africaine des Technologies de l’Information et de la Communication, en abrégé ESATIC est un établissement Public d’enseignement supérieur qui forme des cadres dans le secteur des TIC. Elle est également chargée de faire de la recherche et du développement.

            La création de l’ ESATIC répond à la vision du Président de la République de faire du secteur des TIC l’un des principaux leviers du développement économique et social de notre pays. Au regard du taux de pénétration des TIC qui reste faible dans notre pays et particulièrement dans nos administrations et services, l’ouverture de l’ ESATIC favorisera la vulgarisation des TIC. A travers des partenariats productifs, l’ ESATIC travaillera à mettre sur le marché, des cadres dynamiques et opérationnels.

            La coopération avec les opérateurs du secteur et les régulateurs favorisera une mise à niveau continuelle des parcours pédagogiques. En associant pleinement le secteur privé à cette mission, la recherche à l’ ESATIC se trouvera renforcée et les chercheurs proposeront les services qui répondent parfaitement à l’attente des clients.

            A terme, l’effectif sera de 600 étudiants inscrits en licence et en master. Dans un futur proche, l’ ESATIC développera le volet recherche et mettra en place une école doctorale dans le secteur des TIC. L’accès se fera sur concours. Avec l’appui de tous et de chacun, je reste persuadé que l’ ESATIC deviendra très bientôt le Carrefour Africain des TIC.
          </p>

          <h3>Condition d'admission</h3>
          <p class="text">
            L’admission directe à l’ESATIC se fait par voie de concours, aussi bien pour l’accès au cycle de la Licence que pour l’accès au cycle du Master. Un jury composé de représentants du secteur public, du secteur privé et de celui des Technologies de l’Information et de la Communication (TIC) adopte la liste des candidats autorisés à concourir et proclame les résultats de l’admissibilité puis de l’admission. Les résultats définitifs sont arrêtés par le Ministre
          </p>

